import Axios from 'axios';
import firebase from 'firebase/app'
import 'firebase/auth'
import {authUser, keepUser, PROXY } from '../../constants/config'
import { setCurrentUser, getCurrentUser } from '../../utils';
import { keepToken } from "./../../constants/formKey";

export default {
  state: {
    currentUser:  authUser(),//getCurrentUser(),//isAuthGuardActive ? getCurrentUser() : getCurrentUser(),
    // currentUser: isAuthGuardActive ? getCurrentUser() : currentUser,

    loginError: null,
    processing: false,
    forgotMailSuccess: null,
    resetPasswordSuccess: null
  },
  getters: {
    currentUser: state => state.currentUser,
    processing: state => state.processing,
    loginError: state => state.loginError,
    forgotMailSuccess: state => state.forgotMailSuccess,
    resetPasswordSuccess: state => state.resetPasswordSuccess,
  },
  mutations: {
    setUser(state, payload) {
      state.currentUser = payload
      state.processing = false
      state.loginError = null
    },
    setLogout(state) {
      state.currentUser = null
      state.processing = false
      state.loginError = null
    },
    setProcessing(state, payload) {
      state.processing = payload
      state.loginError = null
    },
    setError(state, payload) {
      state.loginError = payload
      state.currentUser = null
      state.processing = false
    },
    setForgotMailSuccess(state) {
      state.loginError = null
      state.currentUser = null
      state.processing = false
      state.forgotMailSuccess = true
    },
    setResetPasswordSuccess(state) {
      state.loginError = null
      state.currentUser = null
      state.processing = false
      state.resetPasswordSuccess = true
    },
    clearError(state) {
      state.loginError = null
    }
  },
  actions: {
    login({ commit }, payload) {
      commit('clearError')
      commit('setProcessing', true)
      // firebase
      //   .auth()
      //   .signInWithEmailAndPassword(payload.email, payload.password)
      //   .then(
      //     user => {
      //       const item = { uid: user.user.uid, ...currentUser }
      //       setCurrentUser(item)
      //       commit('setUser', item)
      //     },
      //     err => {
      //       setCurrentUser(null);
      //       commit('setError', err.message)
      //       setTimeout(() => {
      //         commit('clearError')
      //       }, 3000)
      //     }
      //   );


        Axios.post(`${PROXY}admin/login`, payload)
        .then(res=>{
            if(!res.data.error){
              // const {authorization} = ;
              localStorage.AT = window// (res.data.data.authorization)
              keepToken(res.data.data.authorization);
              delete res.data.data.authorization;
              const user = res.data.data;
              keepUser(user)
              commit('setUser', user);
            }else{
              setCurrentUser(null);
              commit('setError', "Something went wrong");
            }

        })
        .catch(err=>{
            if(err && err.response && err.response.status === 401){
              setCurrentUser(null);
              commit('setError', err.message)
            }
        })

    },
    forgotPassword({ commit }, payload) {
      commit('clearError')
      commit('setProcessing', true)
      firebase
        .auth()
        .sendPasswordResetEmail(payload.email)
        .then(
          user => {
            commit('clearError')
            commit('setForgotMailSuccess')
          },
          err => {
            commit('setError', err.message)
            setTimeout(() => {
              commit('clearError')
            }, 3000)
          }
        )
    },
    resetPassword({ commit }, payload) {
      commit('clearError')
      commit('setProcessing', true)
      firebase
        .auth()
        .confirmPasswordReset(payload.resetPasswordCode, payload.newPassword)
        .then(
          user => {
            commit('clearError')
            commit('setResetPasswordSuccess')
          },
          err => {
            commit('setError', err.message)
            setTimeout(() => {
              commit('clearError')
            }, 3000)
          }
        )
    },


    signOut({ commit }) {
      firebase
        .auth()
        .signOut()
        .then(() => {
          setCurrentUser(null);
          commit('setLogout')
        }, _error => { })
    }
  }
}
